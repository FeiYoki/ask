                                                            <pre class="replay-info-txt answer_con"><p><strong>因为北方的“秋裤”真的是从秋天开始穿的……冬天穿的，叫棉裤。<br/></strong></p><p>再往北就是秋裤毛裤棉裤穿三条。</p><p><img src="picture/20171128154306-343532317_null_832_537_494879.jpg"/></p>我记得小时候的裤子还不叫秋裤，叫棉毛裤。<p><br/></p><p><br/></p><p><strong>讲讲秋裤是谁发明的吧，其实发明秋裤的是外国人~</strong></p><p>秋裤的历史可以追溯到早年中世纪流行的<strong>马裤</strong>，这种长款内衣大概于17世纪传到英国，在18世纪演变成流行的睡衣，后来为了方便脱裤子上厕所，才重新设计分成上下两件。</p><p><br/></p><p>在第二次世界大战期间，为了抵御欧洲战场的寒冷，秋衣和秋裤是美国陆军的标配，在互联网上搜索，还可以找到大量的军品爱好者出售的美军秋衣秋裤。</p><p><strong>最早取得秋裤设计专利的，据说是加拿大的弗兰克·斯坦菲尔德</strong>。他与自己的兄弟以号称“不缩水”的棉内衣起家，在1915年12月7日正式申请了秋裤的专利，成为<strong>现代商业秋裤之父</strong>。</p><p><br/></p><p><img src="picture/20171128161232-1725800733_null_842_553_661481.jpg"/></p><p><br/></p><p>以北方的节气而论，一般立秋之后，寒露之前便要穿上秋裤了。至迟到立冬，如果再不穿的话，按老理，基本就属于“作死”了。天气转凉，把秋衣扎进秋裤里，然后再把秋裤扎进袜子里，充满安全感。</p><p><br/></p><p><br/></p><p><strong>2008年初的“秋裤门”事件轰动一时。</strong></p><p>某知名时尚杂志女主编，在访谈中透露自己<strong>冬天坚持不穿秋裤</strong>，甚至奉劝老板，出国千万不能穿秋裤，否则会有失中国人形象，因为外国人有秋裤歧视……</p><p>于是，<strong>“穿不穿秋裤”</strong>正式成为一个全民话题，引发网友的持久讨论。</p><p><br/></p><p><br/></p><p><strong>外国人不穿秋裤？</strong></p><p><strong>加拿大</strong>人一般不穿秋裤，而是穿一条比较厚的裤子过冬，如果冬天要到室外工作，会在裤子外面再套一条羽绒裤，这样进到室内可以方便地脱下来。早年，秋裤在美国的高寒地区也有相当的市场。</p><p>现在，平均一人拥有一辆汽车的美国，冬天进屋暖气，出门坐车，完全不必穿秋裤，年轻人更是不穿秋裤。</p><p><br/></p><p>在<strong>韩国</strong>，只有老年人才会穿秋裤，冬季里韩国人的标准装扮是棒球帽，棉大衣，短裤，拖鞋。</p><p>在<strong>日本</strong>，同样只有老年人才会穿秋裤，女孩自然不用说，大雪中上下学一样是小短裙和光腿，留学生到了日本要做的第一件事情是扔秋裤，因为只有50岁以上的女人需要。</p><p><br/></p><p><img src="picture/20171128161718-433277799_null_621_470_488034.jpg"/></p><p><br/></p><p>大雪天还露腿的国家，伤不起……</p><p><br/></p><p><br/></p><p><strong>但是秋裤也可以时尚……</strong></p><p>2012年，韩国总统李明博就在两周一次的广播演说中呼吁民众“穿上秋裤，节约能源”。</p><p>2011年，英国演员裘德洛现身伦敦机场时穿着一条令人炫目的秋裤，再搭配松垮的夹克和T恤，设计师维多利亚-巴特利特在接受《纽约时报》采访时表示：“这是一种非常英伦范儿的打扮。”</p><p>随后，越来越多的重量级时装品牌也在秋冬时装发布会上展示出各式各样的秋裤。</p><p><img src="picture/20171128162530-1752761509_null_445_690_458066.jpg"/></p><p><br/></p><p>秋裤时不时尚主要看颜值……</p><p><br/></p><p><br/></p><p><strong>哪类人应该尽早穿秋裤？</strong></p><p><strong>关节痛</strong>一定要穿秋裤，当气温快速下降后，膝关节的保暖非常重要，秋裤此时就很重要。</p><p><strong>老年人、膝关节才受伤</strong>或者本身就有膝关节病的人，更要早点穿上秋裤。</p><p>还有一类人，他们的腿比一般人容易冷，而且会冰冷冰冷的，这样的人要赶紧穿秋裤保暖了。这种腿脚冰凉，其实是由于下肢血管病变引起的<strong>“老寒腿”</strong>现象，现在年轻人慢性病患者越来越多，“</p><p>老寒腿”也在年轻化，那些30-40岁的人群，要是有点代谢综合征，再不注意腿部保暖，也得提防着。</p><p><br/></p><p><br/></p><p><strong>怎么选秋裤？</strong></p><p>1、看做工</p><p>做工包括最主要的就是裆部，腰部，袖口以及领口处。看这些地方的针线是细密线，还是粗线针织的。如果是粗线针织的，不建议买，因为在做运动的时候，会导致断线裂口。</p><p><img src="picture/20171128162725-542712368_null_772_675_172423.jpg"/></p><p>2、看弹性</p><p>毕竟，我们坐，站，跑，走，如果里面的衣服太硬的话，摩擦身体会很难受，而弹性较好的秋衣秋裤则没有这方面的烦恼。</p><p>3、看材料</p><p>在选择秋衣秋裤的时候，最好看一下秋衣秋裤的材料，是棉的还是纤维的或者是绦纶等。实际上，秋衣秋裤如果想要透气好，则用纯棉的。如果是想要弹性好的话，则纤维的比较好，尤其是大豆纤维的相对来讲，弹性很高。</p><p>4、看品牌</p><p>最后还是不知道怎么选就看品牌吧，选择你熟悉的牌子……</p><p><br/></p><p><strong>有一种寒冷，叫忘穿秋裤……</strong></p><p><img src="picture/20171128163008-495070100_null_882_580_414272.jpg"/></p></pre>
                    
        
